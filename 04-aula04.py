# Operadores Aritméticos
valorA = float(input("Digite o valor de A: "))
valorB = float(input("Digite o valor de B: "))
# Soma
soma = valorA + valorB
print(f"A soma = {soma}")
# Substração
substracao = valorA - valorB
print(f"A substração = {substracao}")
# Multiplicação
multiplicacao = valorA * valorB
print(f"A multiplicação = {multiplicacao} ")
# Divisão
divisao = valorA / valorB
print(f" A divisão = {divisao}")
# Divisão inteira 
divisao_inteira = valorA // valorB
print(f" A divisão inteira = {divisao_inteira}")
# Resto da divisão
resto_divisao = valorA % valorB
print(f"O resto da divisão = {resto_divisao}")
# Exponenciação
exponenciacao = valorA ** valorB
print(f"A exponenciação = {exponenciacao}")